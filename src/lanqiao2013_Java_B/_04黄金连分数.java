package lanqiao2013_Java_B;

import java.math.BigDecimal;
import java.math.BigInteger;

//1/化为求斐波那锲相邻两项的比值，n的项数无关，求到小数点后101位，多次求找到不变的
//2、double无法表示100位小数，BigInteger、BigDecimal

public class _04黄金连分数 {
    public static void main(String[] args) {

        BigInteger a=BigInteger.ONE;
        BigInteger b=BigInteger.ONE;
        for (int i = 3; i < 500; i++) {
            BigInteger t=b;
            b=a.add(b);
            a=t;
        }
        BigDecimal divide = new BigDecimal(a,110).divide(new BigDecimal(b,110),BigDecimal.ROUND_HALF_DOWN);
        System.out.println(divide.toPlainString().substring(0,103));//共103个字符，去掉0. 还有101个字符 然后四舍五入


    }

}


